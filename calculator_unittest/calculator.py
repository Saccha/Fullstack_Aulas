class Calculadora():
    """Performs the four basic mathematical operations
    Methods:
     add(number, number)
     subtract(number, number)
     multiply(number, number)
     divide(number,number)
    """

    def add(self, x, y):
        """Adds two numbers together
        Arguments:
          x - Any number
          y - Any number
        """
        return x + y
    def sub(self, x, y): 
        """Subtracts two numbers together
        Arguments:
          x - Any number
          y - Any number
        """
        return x - y
    def mult(self, x, y):
        """Multiplies two numbers together
        Arguments:
          x - Any number
          y - Any number
        """
        return x * y
    def div(self, x, y):   
        """Divides two numbers together
        Arguments:
          x - Any number
          y - Any number
        """
        return x / y